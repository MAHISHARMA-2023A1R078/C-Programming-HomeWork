//title of program
//Program to print sum of 10 natural numbers using for loop
#include<stdio.h>
//function heading
void main()
{
//variable declaration
int i,sum;
//condition of For Loop
for(i=1;i<=10;i++)
{
sum=sum+i;
//output statement 
printf("sum=sum+i\n");
printf("%d\n",sum);
}
}