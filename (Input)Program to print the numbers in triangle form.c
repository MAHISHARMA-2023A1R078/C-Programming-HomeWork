//titile of program
//Program to print the numbers in triangle form
#include<stdio.h>
//header file
void main()
{
//variable declaration
int i,j;
//condition to print the umbers in triangle form
for(i=0;i<=5;i++)
{
for(j=0;j<=i;j++)
{
//output statement 
printf("%d",i);
}
//output statement 
printf("\n");
}
}
