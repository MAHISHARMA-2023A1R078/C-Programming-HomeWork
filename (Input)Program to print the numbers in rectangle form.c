//Program to print the numbers in rectangle form
#include<stdio.h>
//header file
void main()
{
//variable declaration
int i,j;
//condition to print the umbers in rectangle form
for(i=0;i<=5;i++)
{
for(j=0;j<=5;j++)
{
//output statement 
printf("%d",j);
}
//output statement 
printf("\n");
}
}
